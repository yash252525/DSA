# Negative indexing  
thislist = ["apple", "banana", "cherry"]
print(thislist[-1])

# Will include list items from index 0-3, 4 is excluded
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[:4])

# from 2 to entire list
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:])

